    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
        model.setRowCount(0);
        for(Product w: wr.getOrderList()){
            Object[] row = new Object[2];
            row[0] = w.getName();
            row[1] = w.getNumber();
            pList.add(w);
            model.addRow(row);
        }        
    }

