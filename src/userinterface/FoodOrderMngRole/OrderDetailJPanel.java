    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        if(wr.getStatus().equals("Placed Order")){
            for(Product p: pList){
                for(Product d: foodEnterprise.getFoodList()){
                    if(d.getName().equals(p.getName())){
                        int delete_num = p.getNumber();
                        int old_num = d.getNumber();
                        d.setNumber(old_num - delete_num);
                        break;
                    }
                }
            }
        }
        else if(wr.getType().equals("approved")){
            wr.setType("Exchange");
        }

        int old_balance = foodEnterprise.getEnterprise_Balance();
        foodEnterprise.setEnterprise_Balance(old_balance + wr.getPrice());
        
        wr.setStatus("Ready");
        JOptionPane.showMessageDialog(null, "Product Reset Successfully and ready for pick up by deliveryCompany!");
    }//GEN-LAST:event_btnConfirmActionPerformed


